# Questão 02) Dada a lista frutas = ['maçã','banana','laranja','uva'], remova o elemento 'laranja' e imprima a lista atualizada.

# Criando uma lista com classes strings de frutas.
lista_frutas = ['Maçã', 'Banana', 'Laranja', 'Uva']

# Usando o método "remove" para remover a string "Laranja" da lista.
lista_frutas.remove('Laranja')

print(f'Lista atualizada: \n{lista_frutas}')
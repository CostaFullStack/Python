# Questão 06) Dado o dicionário frutas = {'maçã': 3, 'banana': 5, 'uva': 2}, adicione 2 à quantidade de maçãs e imprima o dicionário atualizado.

# Criando um dicionário com nome "frutas".
frutas = {
# Utilizando a classe "str" e adicionando as "chaves" com seus respectivos "valores", utilizando a classe "str", contendo números com a classe "int".
'maçã': 3, 
'banana': 5, 
'uva': 2}

# Somando mais dois ao "valor" da entrada com "chave" igual a "maçã" do dicionário "frutas".
frutas['maçã'] += 2

# Mostrando na tela o dicionário atualizado com o seu respectivo "valor" adicionado.
print(f'Dicionário atualizado: \n{frutas}')